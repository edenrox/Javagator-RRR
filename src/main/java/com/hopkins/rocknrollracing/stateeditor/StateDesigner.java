    private void setCarImage() {
        int modelIndex = Integer.parseInt(getSelectedButton(carModelBG).getActionCommand());
        int colorIndex = Integer.parseInt(getSelectedButton(colorBG).getActionCommand());
        
        CarState cs = new CarState();
        cs.frame = 2;
        cs.color = CarColor.All[colorIndex];
        cs.model = CarModel.All[modelIndex];
        
        Graphics g = carImage.createGraphics();
        g.clearRect(0,0, carImage.getWidth(), carImage.getHeight());
        carElement.render(g, cs);
        g.dispose();
        
        carImagePanel.setImage(carImage);
    }
    
    private AbstractButton getSelectedButton(ButtonGroup bg) {
        Enumeration<AbstractButton> buttons = bg.getElements();
        while (buttons.hasMoreElements()) {
            AbstractButton button = buttons.nextElement();
            if (button.isSelected()) {
                return button;
            }
        }
        return null;
    }
    
    private void setPlanetImage() {
        AbstractButton button = getSelectedButton(planetBG);
        String planetName = button.getActionCommand();
        planetName = planetName.toLowerCase().replace(" ", "_");
        String filename = String.format("images/planets/%s.png", planetName);
        
        try {
            InputStream imageIS = this.getClass().getClassLoader().getResourceAsStream(filename);
            BufferedImage image = ImageIO.read(imageIS);
            planetImagePanel.setImage(image);
        } catch (Exception ex) {
            System.err.println("Error: " + ex.toString());
        }
    }
    
