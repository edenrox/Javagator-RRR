    private void initValues() {
        ButtonGroup[] toReset = new ButtonGroup[] {
            difficultyBG, planetBG, divisionBG,
            carModelBG, colorBG, tiresBG,
            engineBG, shocksBG, armorBG
        };
        JSpinner[] spinners = new JSpinner[] {
            chargesWeapon, chargesBoost, chargesDrop
        };
        
        for(ButtonGroup group : toReset) {
            selectFirst(group);
        }
        
        for(JSpinner spinner : spinners) {
            spinner.setValue(1);
        }
        
        money.setText("10000");
        
        setPlanetImage();
        setCarImage();
    }
    
    private void selectFirst(ButtonGroup bg) {
        AbstractButton firstButton = bg.getElements().nextElement();
        firstButton.setSelected(true);
    }
    
    
    
